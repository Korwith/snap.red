function loadSidebar(name) {
    let this_data = data[name].images;
    for (var i in this_data) {
        let date_split = i.split('/');
        let month_num = date_split[0];
        let month = n_to_month[month_num];
        let year_num = date_split[2];

        let month_select = nav_select.querySelector(`.nav_button[month="${month}"][year="${year_num}"]`);
        let year_seperator = nav_select.querySelector(`hr[year="${'20' + year_num}"]`);

        if (!year_seperator) {
            let hr = document.createElement('hr');
            hr.setAttribute('year', `20${year_num}`);
            nav_select.appendChild(hr);
        }
        if (month_select) { continue };

        let button_clone = nav_placeholder.cloneNode(true);
        let button_text = button_clone.querySelector('.section');
        button_text.innerHTML = `${month} (${getMonthCount(i, name)})`;
        button_clone.setAttribute('month', month);
        button_clone.setAttribute('year', year_num);
        button_clone.removeAttribute('id');
        button_clone.onclick = timeSelect;
        nav_select.appendChild(button_clone);
    }

}

let scroll_image_index = 0;
function loadImages(name) {
    let this_data = data[name];
    let images = this_data.images;
    let image_keys = Object.keys(images);
    if (!image_keys[9 * (scroll_image_index)]) { return false };

    for (var i = 0; i < image_keys.length; i++) {
        let this_key = image_keys[i];
        if (i >= 9 * scroll_image_index && i < 9 * (scroll_image_index + 1)) {
            if (images[this_key].featured) { continue };
            makeAlbum(name, this_key);
        }
    }

    scroll_image_index++;
}

function loadFeatured(name) {
    let this_data = data[name];
    let images = this_data.images;
    
    for (var i in images) {
        let this_day = images[i];
        if (!this_day.featured) { continue };
        makeAlbum(name, i, false, this_day.featured);
    }
}

function generateTo(month, year) {
    function tryFind() {
        loadImages(selected_user);
        let first_entry = entry_grid.querySelector(`.entry[month="${month}"][year="${year}"]`);
        if (!first_entry) {
            tryFind();
        }
    }

    tryFind();
    return entry_grid.querySelector(`.entry[month="${month}"][year="${year}"]`);
}

function handleScroll(event) {
    let content_rect = content.getBoundingClientRect();
    let scroll_bottom = content.scrollTop + content_rect.height;

    if (scroll_bottom + 100 > content.scrollHeight) {
        loadImages(selected_user);
    }
}
content.addEventListener('scroll', handleScroll);

function loadPerson(name) {
    cleanup();
    let this_data = data[name];
    let images = this_data.images;

    loadCard(name);
    updateUserSelect(name);
    loadFeatured(name);
    loadImages(name);
    loadSidebar(name);

    profile_button_text.innerHTML = `Profile (${Object.keys(images).length})`;
    selected_user = name;

    if (user_select_menu.classList.contains('toggle')) {
        handleUserSelect();
    }

    if (this_data.videos) {
        loadVideos(name);
    } else {
        all_video.classList.add('hide');
    }

    if (name == 'Thaddeus') {
        site_ad.classList.remove('hide');
    } else {
        site_ad.classList.add('hide');
    }
}

function cleanup() {
    let grid_content = entry_grid.querySelectorAll('.entry');
    let us_content = user_select_menu.querySelectorAll('.user_select');
    let social_content = social_holder.querySelectorAll('a');
    let sidebar_content = nav_select.querySelectorAll('hr, .nav_button');
    let video_content = video_holder.querySelectorAll('.entry');
    scroll_image_index = 0;

    for (var i = 0; i < grid_content.length; i++) {
        entry_grid.removeChild(grid_content[i]);
    }

    for (var i = 0; i < us_content.length; i++) {
        if (us_content[i].classList.contains('top')) { continue };
        user_select_menu.removeChild(us_content[i]);
    }

    for (var i = 0; i < social_content.length; i++) {
        social_holder.removeChild(social_content[i]);
    }

    for (var i = 0; i < sidebar_content.length; i++) {
        if (sidebar_content[i].classList.contains('profile_button')) { continue };
        nav_select.removeChild(sidebar_content[i]);
    }

    for (var i = 0; i < video_content.length; i++) {
        video_content[i].remove();
    }
}

function readPushState() {
    if (window.location.href.includes('#id=')) {
        let detected_user = window.location.href.split('#id=')[1].toLowerCase();
        let upper = detected_user.charAt(0).toUpperCase() + detected_user.slice(1);

        if (data[upper]) {
            loadPerson(upper);
        } else {
            loadPerson('Thaddeus');
            history.pushState(null, null, window.location.href.split('#id=')[0]);
        }
    } else {
        loadPerson('Thaddeus');
    }
}

function getMonthCount(date, name) {
    let date_split = date.split('/');
    let month = date_split[0];
    let year = date_split[2];

    let this_data = data[name].images;
    let count = 0;

    for (var i in this_data) {
        let this_split = i.split('/');
        let this_month = this_split[0];
        let this_year = this_split[2];

        if (month != this_month) { continue };
        if (year != this_year) { continue };
        count++;
    }

    return count;
}

function makeAlbum(name, date, parent, order) {
    let this_data = data[name].images[date];
    let first_preview = this_data.id[0];
    let clone = entry_placeholder.cloneNode(true);
    let clone_date = clone.querySelector('.text.date');
    let clone_title = clone.querySelector('.text.title');

    loadBackgroundImage(clone, `media/preview/${name}/IMG_${first_preview}.jpg`);
    clone_date.innerHTML = date;
    clone_title.innerHTML = this_data.name;
    clone.setAttribute('date', date);
    clone.removeAttribute('id');
    clone.onclick = photoSelect;

    let date_split = date.split('/');
    let month = n_to_month[date_split[0]];
    let year = date_split[2];
    clone.setAttribute('month', month);
    clone.setAttribute('year', year);

    if (!parent) {
        entry_grid.appendChild(clone);
    } else {
        parent.appendChild(clone);
    }
    
    if (order) {
        clone.style.order = 1 - order;
        clone.classList.add('featured');
    }
}

function loadBackgroundImage(element, url) {
    element.classList.add('loading');
    let attempt = new Image();
    attempt.onload = function() {
        element.classList.remove('loading');
        element.style.backgroundImage = `url(${url})`;
        attempt.remove();
    }
    attempt.onerror = function() {
        attempt.remove();
    }
    attempt.src = url;
}

function updateUserSelect(name) {
    for (var i in data) {
        if (i == name) { continue };
        // add another thing
        let user_data = data[i];
        let us_clone = user_select_placeholder.cloneNode(true);
        let us_icon = us_clone.querySelector('.icon');
        let us_name = us_clone.querySelector('.username');

        us_name.innerHTML = i;
        us_icon.style.backgroundImage = `url(${user_data.card.icon})`;
        us_clone.removeAttribute('id');
        user_select_menu.appendChild(us_clone);

        (function (us_clone, i) {
            us_clone.onclick = function () {
                loadPerson(i);
                history.pushState(null, null, '#id=' + i.toLowerCase());
            };
        })(us_clone, i);
    }
}

function loadVideos(name) {
    let this_data = data[name].videos;
    all_video.classList.remove('hide');

    for (var i = this_data.length - 1; i >= 0; i--) {
        let this_link = document.createElement('a');
        let video_data = this_data[i];
        let this_clone = entry_placeholder.cloneNode(true);
        let this_date = this_clone.querySelector('.text.date');
        let this_name = this_clone.querySelector('.text.title');
        let this_location_icon = this_clone.querySelector('.icon.location');

        this_link.setAttribute('target', '_blank');
        this_link.setAttribute('href', video_data.link);
        this_name.innerHTML = video_data.name;
        this_date.innerHTML = video_data.date;
        this_clone.style.backgroundImage = `url(video/${video_data.thumbnail})`;
        this_location_icon.classList.remove('location');
        this_location_icon.classList.add('video');
        this_clone.classList.add('video');
        this_clone.removeAttribute('id');
        this_link.appendChild(this_clone);
        video_holder.appendChild(this_link);
    }
}

